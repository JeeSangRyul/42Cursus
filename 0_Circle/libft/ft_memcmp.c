#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*str1;
	unsigned char	*str2;
	size_t			index;

	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	index = 0;
	if (n == 0)
		return (0);
	while (str1[index] == str2[index] && index < n - 1)
		index++;
	return (str1[index] - str2[index]);
}
